package com.melona.form;

import java.io.File;

public class SingerForm {
	
	private String name;
	private File img;
	
	public SingerForm() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public File getImg() {
		return img;
	}

	public void setImg(File img) {
		this.img = img;
	}

}
