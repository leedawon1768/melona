package com.melona.model;

public class MyAlbum {

	private int no;
	private String name;
	private int menberNo;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getMenberNo() {
		return menberNo;
	}
	public void setMenberNo(int menberNo) {
		menberNo = menberNo;
	}
	
	
}
